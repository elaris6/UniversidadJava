package es.local.avanzados.enumeraciones;

/* Las ENUMERACIONES son un tipo especial de clase que se
* utiliza para almacenar varios objetos/elementos.
* En lugar de definirse con la palabra reservada "class", se
* definen con la palabra reservada "enum". */

public enum Enumeraciones {

    /* Los elementos que agregamos en una enumeración por defecto son
    * "public static final", es decir, son CONSTANTES.
    *
    * Su utilidad es para facilitar la creación de listas de constantes
    * cuando son necesarias.
    *
    * Por defecto, cada uno de los valores de la enumeracion son "String". */

    LUNES,
    MARTES,
    MIERCOLES,
    JUEVES,
    VIERNES,
    SABADO,
    DOMINGO
}
